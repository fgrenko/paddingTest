import matplotlib.pyplot as plt
import numpy as np
import pandas as pd
def createDataSetGraph():
    data = pd.read_csv('amp.csv')
    # print(data)
    # form dataframe
    # data = data.head()
    df = pd.DataFrame(data)
    print(df['label'].value_counts())
    df['label'].value_counts().plot(kind="bar")
    plt.title("amp.csv dataset")
    plt.ylabel("Count")
    plt.xlabel("Label")
    # plt.show()
    plt.savefig("amp")
    

def createBoxPlot(data, x_names, x_label, y_label, file_name):
    fig, ax = plt.subplots()
    bp = ax.boxplot(data, showmeans=True)
    plt.xticks([i for i in range(1, len(x_names)+1)], x_names)
    plt.ylabel(y_label)
    plt.xlabel(x_label)

    m = [np.mean(x) for x in data]
    std = [np.std(x) for x in data]

    for i, line in enumerate(bp['medians']):
        x, y = line.get_xydata()[1]
        text = ' μ={:.2f}\n σ={:.2f}'.format(m[i], std[i])
        ax.annotate(text, xy=(x, y), rotation=90)

    plt.title(file_name)
    plt.savefig(file_name)

